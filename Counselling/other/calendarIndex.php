		function edit(event){
			start_event = event.start_event.format('YYYY-MM-DD HH:mm:ss');
			if(event.end_event){
				end_event = event.end_event.format('YYYY-MM-DD HH:mm:ss');
			}else{
				end_event = start_event;
			}
			
			id =  event.id;
			
			Event = [];
			Event[0] = id;
			Event[1] = start_event;
			Event[2] = end_event;
			
			$.ajax({
			 url: 'editEventDate.php',
			 type: "POST",
			 data: {Event:Event},
			 success: function(rep) {
					if(rep == 'OK'){
						alert('Saved');
					}else{
						alert('Could not be saved. try again.'); 
					}
				}
			});
		}
		
	});
